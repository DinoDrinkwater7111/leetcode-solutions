import assert from 'assert';

const funcs = [
    function halvesAreAlike(s: string): boolean {
        const vowels = new Set(['a', 'e', 'i', 'o', 'u', 'A', 'E', 'I', 'O', 'U']);
        let count = 0;
        for(let i=0;i<s.length ;i++){
            if(vowels.has(s[i])) count+= (i << 1) < s.length ? 1 : -1
        }
        return count===0
    }
];

