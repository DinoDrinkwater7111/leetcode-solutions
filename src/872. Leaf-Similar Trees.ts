import assert from 'assert';
import _ from 'lodash';

class TreeNode {
    val: number;
    left: TreeNode | null;
    right: TreeNode | null;
    constructor(val?: number, left?: TreeNode | null, right?: TreeNode | null) {
        this.val = val === undefined ? 0 : val;
        this.left = left === undefined ? null : left;
        this.right = right === undefined ? null : right;
    }
}

const funcs = [
    function leafSimilar(root1: TreeNode | null, root2: TreeNode | null): boolean {
        function getLeafs(node: TreeNode | null, leafs: number[]): number[] {
            if (node === null) return leafs;
            if (node.left === null && node.right === null) leafs.push(node.val);
            getLeafs(node.left, leafs);
            getLeafs(node.right, leafs);
            return leafs;
        }
        const leafs1 = getLeafs(root1, []);
        const leafs2 = getLeafs(root2, []);
        return _.isEqual(leafs1, leafs2);
    },
];

