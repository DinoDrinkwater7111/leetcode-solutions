import assert from 'assert';

class TreeNode {
    val: number;
    left: TreeNode | null;
    right: TreeNode | null;
    constructor(val?: number, left?: TreeNode | null, right?: TreeNode | null) {
        this.val = val === undefined ? 0 : val;
        this.left = left === undefined ? null : left;
        this.right = right === undefined ? null : right;
    }
}

const funcs = [
    function pruneTree(root: TreeNode | null): TreeNode | null {
        function pruneTreeEx(node: TreeNode | null): boolean {
            if (node === null) return false;
            const has1_left = pruneTreeEx(node.left);
            if (!has1_left) node.left = null;
            const has1_right = pruneTreeEx(node.right);
            if (!has1_right) node.right = null;
            return node.val === 1 || has1_left || has1_right;
        }

        const vRoot = new TreeNode(NaN, root);
        pruneTreeEx(vRoot);
        return vRoot.left;
    },
];

