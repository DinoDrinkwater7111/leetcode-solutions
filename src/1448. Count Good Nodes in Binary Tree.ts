import assert from 'assert';

class TreeNode {
    val: number;
    left: TreeNode | null;
    right: TreeNode | null;
    constructor(val?: number, left?: TreeNode | null, right?: TreeNode | null) {
        this.val = val === undefined ? 0 : val;
        this.left = left === undefined ? null : left;
        this.right = right === undefined ? null : right;
    }
}

const funcs = [
    function goodNodes(root: TreeNode | null): number {
        let count = 1;
        const maxStack = [root!.val];
        const stack = [root!.val];
        function goodNodesEx(node: TreeNode | null): void {
            if (node === null) return;
            stack.push(node.val);
            if (stack[stack.length - 1] >= maxStack[maxStack.length - 1]) {
                maxStack.push(node.val);
                count++;
            }
            goodNodesEx(node.left);
            goodNodesEx(node.right);
            if (maxStack[maxStack.length - 1] === stack.pop()) maxStack.pop();
        }
        goodNodesEx(root!.left);
        goodNodesEx(root!.right);
        return count;
    },
];

